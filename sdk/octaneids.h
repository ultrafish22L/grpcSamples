// The various pin identifiers shared between different nodes and node graphs. A pin ID is of type
//      enum PinId
//
// Pin IDs were introduced to have fast pin lookups and to be able to use auto-complete in the IDE
// etc.. They don't represent an actual pin and are meant to be used for referencing only. As a
// consequence, they also don't have a type or any other parameters. The only thing they have is a
// value and a name string. The same ID may be used in different nodes, but must be unique in a
// pin set.
//
// The name strings of pin IDs have always been static, i.e. don't change between versions.
// That means you can use them to index data of pins persistently. Starting with versions
// >2240002 the same applies to the values of pin IDs. It's also guaranteed that pin IDs will never
// be removed.
//
// NOTE: When adding new pin IDs, the name string should be camelCase (e.g. "saturateToWhite"
//       instead of "saturate_to_white").
//
// NOTE: Due to the Max plugin's special converter for old scenes that used to have internal IDs
//       generated by a random hash, there are some pin IDs that will create collisions
//       between old pin IDs and new pin IDs. These are listed below.
