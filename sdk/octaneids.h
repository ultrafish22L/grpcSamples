// The various attribute identifiers shared between different nodes and node graphs. An attribute
// ID is of type
//      enum AttributeId
//
// Attribute IDs were introduced to have fast attribute lookups and to be able to use auto-complete
// in the IDE etc.. They don't represent an actual attribute and are meant to be used for referencing
// only. As a consequence, they also don't have a type or any other parameters. The only thing they
// have is a value and a name string. The same ID may be used in different node items, but must be
// unique in an attribute set.
//
// The name strings of attribute IDs have always been static, i.e. don't change between versions.
// That means you can use them to index data of attributes persistently. Starting with versions
// >2240002 the same applies to the values of attribute IDs. It's also guaranteed that attribute IDs
// will never be removed.
//
// NOTE: When adding new attribute IDs, the name string should be camelCase (e.g. "aspectRatio"
//       instead of "aspect_ratio").
//
// NOTE: Due to the Max plugin's special converter for old scenes that used to have internal IDs
//       generated by a random hash, there are some attribute IDs that will create collisions
//       between old attribute IDs and new attribute IDs. These are listed below.
